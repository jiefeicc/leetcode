            int len = nums.length;
            if (len == 0) {
                return len;
            }
            int[] dp = new int[len];
            Arrays.fill(dp,1);
            int res = 0;
            int pos = 0;
            for (int i=0; i<len; i++) {
                for (int j=0; j<i; j++) {
                    if (nums[i] > nums[j]) {
                        dp[i] = Math.max(dp[i], dp[j]+1);
                    }
                }
                if (dp[i] >= res) {
                    res = dp[i];
                    pos = i;
                }
            }

            int posdp = res;
            List<Integer> list = new ArrayList<>();
            list.add(nums[pos]);
            for (int i=pos-1; i>=1; i--) {
                if (dp[i] == posdp-1) {
                    list.add(nums[i]);
                    posdp = dp[i];
                }
            }
            System.out.println(list);

            return res;
